
package mathfunction;

/**
 *
 * @author kutub uddin
 */
public class MathFunction {

    public static void main(String[] args) {

        
        System.out.println(Math.round(23.250));
        System.out.println(Math.ceil(23.250));
        System.out.println(Math.floor(23.2));
        System.out.println(Math.rint(23.2));
        System.out.println(Math.abs(-23.2));
        System.out.println(Math.round(Math.random()*20));
        System.out.println(Math.pow(8, 3));
        System.out.println(Math.sqrt(16));
        System.out.println(Math.max(20, 15));
        System.out.println(Math.min(20, 10));
        System.out.println(Math.toDegrees(Math.PI/2));
        System.out.println(Math.sin(30));
        System.out.println(Math.cos(90));
        System.out.println(Math.tan(130));
        System.out.println(Math.PI*1);
       
  
        
    }
    
}